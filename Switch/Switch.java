public class Switch {
    public static void main(String[] args) {
        String estacion = "a";
        switch (estacion) {
            case "Invierno":
                System.out.println("Es invierno");
                break;
            case "Otoño":
                System.out.println("Es Otoño");
                break;
            case "Primavera":
                System.out.println("Es Primavera");
            case "Verano":
                System.out.println("Es Verano");
                break;
            default:
                System.out.println("Es otra estación...");

        }
    }
}






